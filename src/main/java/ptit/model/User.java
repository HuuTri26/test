package ptit.model;

public class User {
	private String id;
	private String password;
	public User() {
		super();
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;

	}

	public String getPassword() {
		return password;

	}

	public String setPassword(String password) {
		return this.password = password;

	}

}
